// Matrix selector function
export function getMatrix(laboratory: 'VIDAS' | 'IDEXX', unit: 'nanograms' | 'nanomoles', breedingMethod: 'natural_ai' | 'tci' | 'surgical_ai' | 'frozen'): ProgesteroneMatrix {
  if (laboratory === 'VIDAS') {
    if (unit === 'nanograms') {
      if (breedingMethod === 'surgical_ai') return VIDAS_NANOGRAMS_SURGICAL;
      if (breedingMethod === 'frozen') return VIDAS_NANOGRAMS_FROZEN;
      return VIDAS_NANOGRAMS_NATURAL_TCI;
    } else {
      if (breedingMethod === 'surgical_ai') return VIDAS_NANOMOLES_SURGICAL;
      if (breedingMethod === 'frozen') return VIDAS_NANOMOLES_FROZEN;
      return VIDAS_NANOMOLES_NATURAL_TCI;
    }
  } else {
    if (unit === 'nanograms') {
      if (breedingMethod === 'surgical_ai') return IDEXX_NANOGRAMS_SURGICAL;
      if (breedingMethod === 'frozen') return IDEXX_NANOGRAMS_FROZEN;
      return IDEXX_NANOGRAMS_NATURAL_TCI;
    } else {
      if (breedingMethod === 'surgical_ai') return IDEXX_NANOMOLES_SURGICAL;
      if (breedingMethod === 'frozen') return IDEXX_NANOMOLES_FROZEN;
      return IDEXX_NANOMOLES_NATURAL_TCI;
    }
  }
}